package com.aurionpro.iterative.test;

public class NumberPrinter {
public static void main(String[] args) {
	System.out.println("Number Printer");
	int i=1;
	while(i<=10) {
		System.out.println(i);
		i++;
	}
	int j=10;
	while(j>=1) {
		System.out.println(j);
		j--; //decrementing operator
	}
	
	
}
}
